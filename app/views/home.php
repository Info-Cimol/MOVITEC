                    <h5 class="passo"><span class="text-info">1.</span> Pense num problema de pesquisa</h5>
                    <p>Identifique problemas que você gostaria de resolver ou fatos, fenômenos e /ou situações que você
                        desejaria entender melhor através da realização da iniciação científica. Geralmente o problema
                        aparece em forma de pergunta. O “problema” precisa ser concebido como algo que irá te auxiliar a
                        ter uma visão mais ampla daquilo que você quer descobrir, investigar ou inventar. Guie-se
                        fazendo a seguinte pergunta: O que a equipe do projeto pretende pesquisar, descobrir, investigar
