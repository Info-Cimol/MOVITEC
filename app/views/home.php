                        investigar e inventar?.</p>
                </div>
                <div>
                    <h5 class="passo"><span class="text-primary">2.</span> Monte sua equipe de trabalho</h5>
                    <p>Projetos podem ser individuais ou em dupla de estudantes. É necessário, também, que os projetos
                        tenham um Orientador. Este orientador deverá ser um professor do curso técnico em Informática,
                        sendo de preferência um profissional de áreas relacionadas com o projeto. Caso a equipe julgue
                        necessário, até dois coorientadores poderão se juntar à equipe.</p>
                    <p>É muito importante desenvolver seu projeto com pessoas que sejam comprometidas e interessadas no
                        tema da pesquisa.</p>
                </div>
                <div>
                    <h5 class="passo"><span class="text-info">3.</span> Consulte o regulamento</h5>
                    <p>É muito importante estar atento às regras de pesquisa, por isso leia com bastante atenção o
                        regulamento.</p>
                </div>
                <div class="pb-3">
                    <a href="regras"><button class="btn btn-primary text-center">Regras FEMIC</button></a>
                </div>
                <div>
                    <h5 class="passo"><span class="text-primary">4.</span> Organize o caderno de campo</h5>
                    <p>O caderno de campo é um item obrigatório. É importante que os autores mantenham a escrita dessa
                        ferramenta de pesquisa, pois ela é de suma importância no desenvolvimento de um projeto de
                        sucesso.
                        Inclusive, esse pode ser um excelente momento para a elaboração de um diário de bordo virtual!
                    </p>
                    <p><strong>Atualmente existem vários tipos de apps de diário de bordo virtual! É possível, também,
                            usar pastas e arquivos compartilhados em drives.</strong></p>
                    <p>Cabe lembrar que o diário de bordo serve para registrar e organizar cronologicamente todas as
                        informações do seu projeto, desde as suas primeiras ideias e reflexões para a escolha do tema.
                        Você pode também organizar uma pasta com fotos, fichas e formulários utilizados na sua pesquisa.
                        Registre todas as etapas realizadas de forma detalhada e precisa (indicando datas e locais)
                        todos os fatos, passos, observações, descobertas, indagações, investigações, entrevistas,
                        testes, resultados e análises.</p>
                </div>
                <div>
                    <h5 class="passo"><span class="text-info">5.</span> Saiba sobre questões éticas/segurança</h5>
                    <p>Fizemos uma síntese relacionada às diversas situações envolvendo aspectos éticos em pesquisa.
                        Consideramos importante que todos os trabalhos, façam a leitura.</p>
                    <p class="fs-1 text-center"><strong>?</strong></p>
                </div>
                <div>
                    <h5 class="passo"><span class="text-primary">6.</span> Conduza o projeto de acordo com o método científico</h5>
                    <p>É muito importante que o projeto seja totalmente desenvolvido e conduzido pelos estudantes e
                        acompanhado e supervisionado pelo orientador/coorientador.Ao longo do desenvolvimento, registre
                        todas as etapas anotando detalhadamente e precisamente (indicando datas e locais) todos os
                        fatos, passos, observações, descobertas, indagações, investigações, entrevistas, testes,
                        resultados e análises.</p>
                </div>
                <div>
                    <h5 class="passo"><span class="text-info">7.</span> Elabore o resumo científico</h5>
                    <p>O Resumo do Projeto deve apresentar de forma sintética as etapas do projeto e deve ser capaz de
                        responder às seguintes questões:</p>
                    <ul>
                        <li><strong>Por que você realizou o projeto? (Objetivos ou Questões identificadas)</strong></li>
                        <li><strong>O que foi realizado por você no projeto? (Procedimentos e Métodos
                                adotados)</strong>: não detalhe os materiais utilizados, concentre-se em descrever
                            procedimentos realizados.</li>
                        <li><strong>Quais foram os resultados alcançados? (Observações ou dados pertinentes que
                                permitiram você chegar a sua conclusão)</strong></li>
                        <li><strong>Os seus objetivos foram alcançados? (Conclusões)</strong></li>
                    </ul>
                    <p class="importante fw-bold">IMPORTANTE</p>
                    <ul>
                        <li>O texto do resumo deve se concentrar no trabalho realizado pelo estudante no projeto.</li>
                        <li>O texto do resumo NÃO deve incluir agradecimentos ou mencionar apoiadores do projeto.</li>
                        <li>O texto do resumo deve ser redigido em parágrafo único e respeitar o limite MÁXIMO de 2000
                            caracteres com espaço (entre 250 e 350 palavras).</li>
                        <li>O conteúdo do resumo não deve incluir desenhos, diagramas ou tabelas. Esses elementos são
                            colocados no relatório do projeto.</li>
                        <li>Palavras-chave são palavras ou termos essenciais ou representativos do projeto. Ao escolher
                            estas palavras ou termos, pense naqueles que imediatamente podem ser associados ao seu
                            projeto. Evite repetir palavras que já aparecem no título.</li>
                    </ul>
                    <p><strong>Lembre-se que o resumo deverá ser inserido no no site da mostra durante a submissão do
                            projeto. No entanto, cabe destacar, que na fase de confirmação da participação um resumo
                            atualizado pode ser inserido no site.</strong></p>
                    <p>O resumo dos trabalhos participantes são publicados no livro de Anais da mostra. Todos os
                        trabalhos aprovados terão seus resumos publicados.</p>
                </div>
                <div>
