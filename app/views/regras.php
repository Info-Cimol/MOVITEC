    <main>
        <div class="container">
            <h2 class="text-center p-4">Regras e critérios FEMIC</h2>
            <ol>
                <li>
                    <p>Podem submeter projetos para a FEMIC Júnior estudantes da Educação Infantil e dos Anos Iniciais do Ensino
                        Fundamental (1º ao 5º anos) de escolas públicas e particulares de todo o Brasil ou países estrangeiros.
                    </p>
                </li>
                <li>
                    <p>Podem submeter projetos para a FEMIC Jovem estudantes dos Anos Finais do Ensino Fundamental (6º ao 9º
                        ano), do Ensino Médio e do Ensino Técnico de escolas públicas e particulares de todo o Brasil ou países
                        estrangeiros.</p>
                </li>
                <li>
                    <p>Podem enviar projetos para a FEMIC Mais trabalhos de autoria de:</p>
                    <ul>
                        <li>Professores da Educação Básica</li>
                        <li>Professores e/ou estudantes do Ensino Superior</li>
                        <li>Profissionais diversos</li>
                    </ul>
                </li>
                <li>
                    <p>Os projetos da FEMIC Júnior e Jovem podem ter entre 1 e 3 estudantes e entre 1 e 3 orientadores. Os
                        estudantes podem ser de idades, séries, escolas, cidades ou estados diferentes. Os projetos da FEMIC
                        Mais deverão ter no máximo 6 autores, incluindo o(a) orientador(a), caso ele exista na equipe. Podem ser
                        listados como autores somente aqueles que participaram diretamente da pesquisa ou da elaboração do
                        trabalho e, portanto, em condições de assumir responsabilidade pública pelo seu conteúdo.</p>
                </li>
                <li>
                    <p>A submissão de trabalho é gratuita e deverá ser feita até a data limite publicada no Cronograma da FEMIC.
                    </p>
                </li>
                <li>
                    <p>Cada estudante da FEMIC Júnior e Jovem podem inscrever apenas UM projeto de sua autoria. É obrigatória a
                        presença de pelo menos um autor do trabalho no momento da videoconferência para avaliação que acontece
                        durante a FEMIC.</p>
                </li>
                <li>
                    <p>Os autores de trabalho inscritos na FEMIC Mais podem ter seus nomes em diferentes trabalhos. Porém, a
                        apresentação de cada trabalho deverá acontecer por autores diferentes caso sejam selecionados para
                        apresentação na mesma Sessão de Comunicação e os horários sejam incompatíveis. É obrigatória a presença
                        de pelo menos um autor do trabalho no momento da videoconferência para avaliação do trabalho.</p>
                </li>
                <li>
                    <p>Os projetos da modalidade FEMIC Júnior e Jovem com duração superior a 12 meses, devem informar no ato da
                        inscrição que se tratam de “Projetos de Continuidade do ano anterior”. O início da pesquisa de campo ou
                        da fase experimental ou de prototipagem deverá ser considerado como data inicial do projeto. Projetos
                        realizados pelos estudantes a partir de trabalhos de outros pesquisadores não são considerados
                        “Continuação de Projeto Anterior”, mas o estudante deverá mostrar claramente os elementos do projeto que
                        ele desenvolveu e quais partes do projeto são de autoria de outro pesquisador citando as devidas fontes.
                        Este item independe da participação do estudante em edições anteriores da FEMIC ou em outras mostras
                        científicas.</p>
                </li>
                <li>
                    <p>Sobre a ORIGINALIDADE E AUTORIA PRÓPRIA dos trabalhos.</p>
                    <ul>
                        <li>
                            <p>Todo trabalho aprovado para participação na FEMIC deve ser original, de única e exclusiva autoria
                                das pessoas relacionadas no ato da inscrição e não se tratar de cópia integral ou parcial de
                                textos e trabalhos de autoria de outrem, seja em formato de papel, eletrônico, digital,
                                audiovisual ou qualquer outro meio. Os autores ao inscreverem seus trabalhos na FEMIC assumem
                                ter total conhecimento e compreensão do que é considerado plágio, não apenas a cópia integral do
                                trabalho, mas também parte dele, inclusive de artigos e/ou parágrafos, sem citação do autor ou
                                de sua fonte. Trabalhos que possuam conteúdos copiados sem as devidas referências serão
                                desclassificados por plágio.</p>
                        </li>
                    </ul>
                </li>
                <li>
                    <p>Orientadores podem participar de múltiplos projetos. O orientador deve ter no mínimo 18 anos para assumir
                        a orientação do projeto e devem, no mínimo, estarem cursando um curso superior.</p>
                </li>
                <li>
                    <p>Na inscrição dos trabalhos é necessário inserir um resumo da pesquisa contendo OBRIGATORIAMENTE entre
                        1500 e 2000 caracteres com espaço. Todos os projetos apresentados na FEMIC terão seus resumos publicados
                        nos Anais do evento.</p>
                </li>
                <li>
                    <p>Para a mostra virtual todos os trabalhos da FEMIC Júnior, Jovem e Mais deverão elaborar um pôster
                        científico/banner virtual no formato indicado pela FEMIC.</p>
                </li>
                <li>
                    <p>Para a mostra virtual todos os trabalhos da FEMIC Júnior, Jovem e Mais deverão elaborar um Vídeo com a
                        explicação do trabalho.</p>
                </li>
                <li>
                    <p>Para a mostra virtual todos os trabalhos da FEMIC Júnior, Jovem e Mais deverão elaborar um Relatório
                        científico do trabalho.</p>
                </li>
                <li>
                    <p>É opcional a apresentação do Plano de pesquisa e Diário de bordo.</p>
                </li>
                <li>
                    <p>Os projetos devem estar categorizados em áreas científicas, conforme as áreas científicas da CAPES/CNPQ.
                    </p>
                    <ul>
                        <li>Ciências Exatas e da Terra</li>
                        <li>Ciências Biológicas</li>
                        <li>Engenharias</li>
                        <li>Ciências da Saúde</li>
                        <li>Ciências Agrárias</li>
                        <li>Ciências Sociais Aplicadas</li>
                        <li>Ciências Humanas</li>
                        <li>Linguística, Letras e Artes</li>
                    </ul>
                </li>
                <li>
                    <p>Todos os trabalhos devem preencher a aba: “Ética e segurança”, disponível no Portal FEMIC durante a
                        submissão.</p>
                </li>
                <li>
                    <p>São avaliados apenas projetos que apresentem todos os dados e documentos exigidos. Aqueles que não
                        estejam de acordo com estas regras serão desclassificados.</p>
                </li>
                <li>
                    <p>Serão selecionados cerca de 200 trabalhos para participarem da FEMIC no ano vigente (previsão de 40 na
                        modalidade Júnior, 120 na modalidade jovem e 40 na modalidade Mais).</p>
                </li>
                <li>
                    <p>Projetos indicados por Feiras Afiliadas à FEMIC deverão realizar a submissão do trabalho até a data
                        limite publicada no Cronograma da FEMIC.</p>
                </li>
                <li>
                    <p>Os projetos selecionados para participarem da FEMIC são anunciados na sua página oficial na data
                        divulgada no Cronograma da feira.</p>
                </li>
                <li>
                    <p>Na modalidade FEMIC Júnior e Jovem a apresentação do projeto deve ser realizada pelo(s) estudante(s)
                        autor(es). O orientador ou o coorientador não poderão apresentar o projeto no lugar dos estudantes. Essa
                        regra não se aplica à FEMIC Mais.</p>
                </li>
                <li>
                    <p>Na mostra de trabalhos virtual e nas videoconferências de avaliação dos trabalhos por um Comitê de
                        Avaliação que julgará os seguintes critérios: Atitudes; Habilidades; Criatividade / Inovação;
                        Relevância; Profundidade; Aplicação do Método; Pôster; Interação durante a apresentação oral.</p>
                </li>
                <li>
                    <p>A FEMIC não se responsabilizará por problemas técnicos e operacionais envolvendo tecnologias de
                        informação e interação que impedirem estudantes de finalizarem suas inscrições/cadastros/submissões ou
                        de se conectarem às atividades da FEMIC.</p>
                </li>
                <li>
                    <p>Com base nas avaliações do Comitê de Avaliação, a organização da FEMIC atribuirá premiações diversas.</p>
                </li>
                <li>
                    <p>As premiações com credenciais para participação em feiras conveniadas à FEMIC serão conferidas aos
                        trabalhos conforme as regras específicas de cada uma delas.</p>
                </li>
                <li>
                    <p>A FEMIC não oferece ajuda de custo, passagens e hospedagens para participação dos autores nos eventos que
                        conquistarem credenciais como premiação.</p>
                </li>
                <li>
                    <p>Quanto aos direitos Intelectuais (Autorais, Imagens, Marcas e afins)</p>
                    <ol>
                        <li>
                            <p>Os expositores ao inscreverem os trabalhos na FEMIC assumem ter total conhecimento e compreensão
                                de que estão autorizando a utilização de quaisquer direitos intelectuais (autorais, imagem,
                                marca e afins) relativos a obras literárias, artísticas ou científicas, obras audiovisuais,
                                textos de palestras, fotografias etc., conteúdos, direitos de personalidade, interpretações,
                                execuções, marcas, sinais distintivos, a qualquer título, de suas autorias ou propriedade,
                                relacionados ao evento na proporção e extensão de suas respectivas criações e participações.</p>
                        </li>
                        <li>
                            <p>Os expositores ao inscreverem os trabalhos na FEMIC assumem ter total conhecimento e compreensão
                                de que estão autorizando a uso do seus nome, depoimentos e imagens (fotos e voz), incluindo
                                fotos e vídeos enviados por estes à FEMIC, para fins de pesquisa, eventos, divulgações,
                                propagandas e retrospectivas relacionadas ao referido evento da FEMIC e seus colaboradores,
                                patrocinadores, atendendo às mesmas finalidades do parágrafo anterior, a título gratuito e por
                                tempo indeterminado. As imagens/ fotografias e sons poderão ser fixados, reproduzidos e
                                modificados com qualquer meio técnico. Poderão ser reproduzidas parcialmente ou na sua
                                totalidade em qualquer suporte (papel, digital, magnético, tecido, plástico, etc.) e integradas
                                a qualquer outro material (fotografia, desenho, ilustração, pintura, vídeo, animação, etc.)
                                conhecido ou que venha a existir no futuro.</p>
                        </li>
                        <li>
                            <p>Os expositores ao inscreverem os trabalhos na FEMIC assumem ter total conhecimento e compreensão
                                de que estão autorizando as descrições citadas nos parágrafos acima a título gratuito, sem que
                                seja devida qualquer remuneração e por tempo indeterminado, podendo ser explorados em qualquer
                                parte do mundo e em todos os ramos (publicidade, edição, imprensa, design, etc.).</p>
                        </li>
                        <li>
                            <p>Os organizadores/responsáveis pela FEMIC são isentos de de quaisquer responsabilidades acerca de
                                qualquer direito demandado relativo a autorizações de Direitos Intelectuais, inclusive quanto a
                                eventuais reclamações de terceiros interessados, valendo a mesma exclusivamente para o evento
                                supracitado e, posteriormente, aos materiais eventualmente veiculados e atrelados ao mesmo.</p>
                        </li>
                        <li>
                            <p>Todos os estudantes, orientadores e coorientadores participantes da FEMIC, obrigatoriamente,
                                deverão preencher, on-line, uma Autorização para uso de Direitos Intelectuais (autorais, imagem,
                                marca e afins). Esse preenchimento ficará disponível no PORTAL FEMIC no ato do cadastro no
                                sistema.</p>
                        </li>
                    </ol>
                </li>
                <li>
                    <p>Em nenhuma situação, caberá recurso quanto aos processos de avaliação conduzidos.</p>
                </li>
                <li>
                    <p>Quanto ao feedback dos processos avaliativos informamos que tanto na etapa de pré-avaliação, que acontece
                        para selecionar os trabalhos participantes, quanto na etapa de avaliação, que acontece para destacar os
                        trabalhos que serão reconhecidos através das premiações conferidas na cerimônia de premiação, a FEMIC
                        praticará o prazo de 60 dias uteis para o envio dos resultados para os autores sobre a avaliação que foi
                        conduzida pela comissão avaliadora. Observar os processos avaliativos na página: Avaliação. O feedback
                        fica disponível no Portal FEMIC.
                    </p>
                </li>
                <li>
                    <p>Todos os participantes devem ler e concordar com a política de privacidade da FEMIC como pré-requisito
                        para sua inscrição. Todos os participantes devem se cadastrar durante a submissão do projeto e não é
                        permitida a troca, alteração ou inclusão de participantes após a submissão do projeto.
                    </p>
                </li>
                <li>
                    <p>A organização da FEMIC se reserva no direito de modificar ou ajustar, a qualquer momento, as regras
                        acima.
                    </p>
                </li>
            </ol>
